CrossBomb::CrossBomb()
{
    //param_.SPR_ITEM = &sprItem_Sword;
    param_.SPR_ITEM = nullptr;
    //param_.SIZE = { BG::CHIP_SIZE * 0.5f, BG::CHIP_SIZE * 0.5f };
    param_.SIZE = { 1, 1 };
    param_.SCALE = { 1,1 };
    param_.ATTACK_BOX = { -28, -28, 28, 28 };
}

void CrossBomb::hit(OBJ2D* src, OBJ2D* dst) const
{
    if (dst->behavior_->getType() != OBJ_TYPE::PLAYER) return;

    VECTOR2 surroundingBlockPos[4] = {
        {src->transform_->position_.x - this->getParam()->SIZE.x,src->transform_->position_.y},                             //左
        {src->transform_->position_.x                           ,src->transform_->position_.y - this->getParam()->SIZE.y},  //上
        {src->transform_->position_.x + this->getParam()->SIZE.x,src->transform_->position_.y},                             //右
        {src->transform_->position_.x                           ,src->transform_->position_.y + this->getParam()->SIZE.y},  //下
    };



    src->behavior_ = nullptr;
}

NormalBomb::NormalBomb()
{
    //param_.SPR_ITEM = &sprItem_Key;
    param_.SPR_ITEM = nullptr;
    //param_.SIZE = { BG::CHIP_SIZE * 0.5f, BG::CHIP_SIZE * 0.5f };
    param_.SIZE = { 1, 1 };
    param_.SCALE = { 1,1 };
    param_.ATTACK_BOX = { -28, -28, 28, 28 };
}

void NormalBomb::hit(OBJ2D* src, OBJ2D* dst) const
{
    if (dst->behavior_->getType() != OBJ_TYPE::PLAYER) return;

    if (dst->actorComponent_)
        dst->actorComponent_->keyNum_++;

    src->behavior_ = nullptr;
}

