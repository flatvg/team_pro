void BaseBombBehavior::move(OBJ2D* obj) const
{
    switch (obj->state_)
    {
    case 0:
        //////// 初期設定 ////////
        obj->transform_->scale_ = getParam()->SCALE;
        obj->renderer_->data_ = getParam()->SPR_ITEM;
        obj->collider_->size_ = getParam()->SIZE;

        obj->collider_->judgeFlag_ = true;
        //obj->collider_->isDrawAttackRect_ = true;

        obj->itemComponent_->origin_ = obj->transform_->position_;

        obj->collider_->calcAttackBox(getParam()->ATTACK_BOX);

        obj->state_++;
        break;
    case 1:
        //////// 通常時 ////////

        // アイテムを揺らす
        obj->transform_->position_.y = obj->itemComponent_->origin_.y + sinf(obj->itemComponent_->angle_) * 4;
        obj->collider_->calcAttackBox(getParam()->ATTACK_BOX);
        obj->itemComponent_->angle_ += ToRadian(4.0f);
        if (obj->itemComponent_->angle_ > DirectX::XM_PI)
            obj->itemComponent_->angle_ += -2 * DirectX::XM_PI;

        break;
    }
}

